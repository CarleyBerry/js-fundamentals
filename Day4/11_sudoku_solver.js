const solve = b => {
  for (let r=0; r<9; r++) for (let c=0; c<9; c++) if (b[r][c] == '.') {
    for (let v=1; v<=9; v++) {
      const s = ''+v, ok = !b[r].includes(s) && !b.some(x => x[c] == s) && 
        !b.some((x, i) => Math.floor(i/3) == Math.floor(r/3) && x.some((y, j) => Math.floor(j/3) == Math.floor(c/3) && y == s));
      if (ok && (b[r][c] = s, solve(b))) return 1; b[r][c] = '.';
    }
    return 0;
  }
  return 1;
};
